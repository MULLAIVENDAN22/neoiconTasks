let logicCount = [-1],
  timingCount = [-1];

document.addEventListener("DOMContentLoaded", () => {
  const logicproject = document.querySelector(".logicproject");
  const logicArrows = logicproject.querySelectorAll(".arrow");

  nextPage(logicproject, logicCount, logicprojectArray);

  logicArrows[0].addEventListener("click", () => {
    perviousPage(logicproject, logicCount, logicprojectArray);
  });
  logicArrows[1].addEventListener("click", () => {
    nextPage(logicproject, logicCount, logicprojectArray);
  });

  const timingproject = document.querySelector(".timingproject");
  const timingArrows = timingproject.querySelectorAll(".arrow");

  nextPage(timingproject, timingCount, timingprojectArray);

  timingArrows[0].addEventListener("click", () => {
    perviousPage(timingproject, timingCount, timingprojectArray);
  });
  timingArrows[1].addEventListener("click", () => {
    nextPage(timingproject, timingCount, timingprojectArray);
  });
});

function nextPage(e, count, array) {
  if (!(count[0] + 1 < array.length)) {
    return;
  } else {
    count[0]++;
    e.querySelector("h1").textContent = count[0] < 10 ? "0" + (count[0]+1) : count[0];
    e.querySelector("h2").textContent = array[count[0]].h2;
    e.querySelector("p").textContent = array[count[0]].p;
    e.querySelector("h6").textContent = array[count[0]].h6;
    e.querySelectorAll("a")[0].setAttribute("href", array[count[0]].a1);
    e.querySelectorAll("a")[1].setAttribute("href", array[count[0]].a2);
    e.querySelector("img").setAttribute("src", array[count[0]].img);
  }
}
function perviousPage(e, count, array) {
  if (!(count[0] - 1 > -1)) {
    return;
  } else {
    count[0]--;
    e.querySelector("h1").textContent = count[0] < 10 ? "0" + (count[0]+1) : count[0];
    e.querySelector("h2").textContent = array[count[0]].h2;
    e.querySelector("p").textContent = array[count[0]].p;
    e.querySelector("h6").textContent = array[count[0]].h6;
    e.querySelectorAll("a")[0].setAttribute("href", array[count[0]].a1);
    e.querySelectorAll("a")[1].setAttribute("href", array[count[0]].a2);
    e.querySelector("img").setAttribute("src", array[count[0]].img);
  }
}

